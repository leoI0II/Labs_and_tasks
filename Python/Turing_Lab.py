#Turing_Lab
memory = list(input())
index = 0
q = 'q1'

table = {
    'q1': {'0': ['0', 1, 'q1'], '1': ['1', 1, 'q1'], '2': ['2', 1, 'q1'], '+':['+', 1, 'q2'], '-': ['-', 1, 'q3'], ' ':[' ', 1, 'q1']},
    'q2': {'0': ['0', 0, 'q9'], '1': ['1', -1, 'q4'], '2': ['2', -1, 'q5']},
    'q3': {'0': ['0', 0, 'q9'], '1': ['1', -1, 'q6'], '2': ['2', -1, 'q7']},
    'q4': {'0': ['1', 0, 'q9'], '1': ['2', 0, 'q9'], '2': ['0', -1, 'q4'], '+':['+', -1, 'q4'], ' ':['1', 0, 'q9']},
    'q5': {'0': ['2', 0, 'q9'], '1': ['0', -1, 'q4'], '2': ['1', -1, 'q4'], '+':['+', -1, 'q5']},
    'q6': {'0': ['2', -1, 'q6'], '1': ['0', -1, 'q8'], '2': ['1', -1, 'q8'], '-': ['-', -1, 'q6']},
    'q7': {'0': ['1', -1, 'q6'], '1': ['2', -1, 'q6'], '2': ['0', 0, 'q9'], '-': ['-', -1, 'q7']},
    'q8': {'0': ['0', -1, 'q8'], '1': ['1', -1, 'q8'], '2': ['2', -1, 'q8'], ' ':[' ', 1, 'q10']},
    'q10': {'0': [' ', 1, 'q10'], '1': ['1', 0, 'q9'], '2': ['2', 1, 'q9'], '-':['-', -1, 'q10'], ' ':['0', 0, 'q9']}
}

while q != 'q9':
    sym, delta, state = table[q][memory[index]]
    memory[index] = sym
    index += delta
    q = state

print(''.join(memory))